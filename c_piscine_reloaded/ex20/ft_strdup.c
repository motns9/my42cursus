#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	length;

	length = 0;
	while (str[length] != '\0')
	{
		length++;
	}
	return (length);
}

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	int		size;
	char	*duplicate;

	size = ft_strlen(src);
	duplicate = (char *)malloc(sizeof(char) * (size + 1));
	if (duplicate == NULL)
	{
		return (NULL);
	}
	ft_strcpy(duplicate, src);
	return (duplicate);
}
